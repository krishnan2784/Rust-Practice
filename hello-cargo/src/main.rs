fn main() {
    println!("Hello, world!");
    let number: u32 = 14;
    println!("The number is {}.", number);
    // Declare a variable
let a_number;
    
// Declare a second variable and bind the value
let a_word = "Ten";
    
// Bind a value to the first variable
a_number = 10;

println!("The number is {}.", a_number);
println!("The word is {}.", a_word);
}
